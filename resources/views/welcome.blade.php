<script>
    document.addEventListener("DOMContentLoaded", function () {
        const elements = document.querySelectorAll(".fade-in");

        function onScroll() {
            elements.forEach((el) => {
                const rect = el.getBoundingClientRect();
                if (rect.top <= window.innerHeight * 0.8) {
                    el.classList.add("visible");
                }
            });
        }

        window.addEventListener("scroll", onScroll);
        onScroll();
    });
</script>
<script>
document.addEventListener("DOMContentLoaded", function() {
    const selectMenu = document.getElementById("code-select");

    // Evento de cambio para navegar al slide seleccionado
    selectMenu.addEventListener("change", function() {
        const targetSlideId = selectMenu.value; // Obtiene el valor del menú desplegable

        // Navegar al slide correspondiente usando el índice
        const carousel = new bootstrap.Carousel('#portfolioCarousel');
        const targetSlide = document.getElementById(targetSlideId);
        
        // Verificar si el slide existe antes de intentar cambiar
        if (targetSlide) {
            const index = [...targetSlide.parentElement.children].indexOf(targetSlide);
            carousel.to(index);  // Cambiar al slide seleccionado
        }
    });
});
</script>
